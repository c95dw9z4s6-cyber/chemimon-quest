def arrange(correct: str, distractors: list[str], target: int):
    assert len(distractors) == 3
    opts = distractors.copy()
    opts.insert(target, correct)
    assert len(set(opts)) == 4, opts
    return opts, target


def feedback(options: list[str], answer: int, focus: str):
    return ['正解です。' if i == answer else f'{focus}を確認してください。' for i in range(4)]


def basic_q(index: int, *, q: str, correct: str, wrong: list[str], explanation: str,
            category='redox', cognitive='reasoning', objective='金属・酸化還元反応を条件と生成物から判断する',
            seconds=40, difficulty=2, scope='stage10-prep', negative=False):
    target = index % 4
    options, answer = arrange(correct, wrong, target)
    item = {
        'q': q,
        'options': options,
        'answer': answer,
        'explanation': explanation,
        'scope': scope,
        'id': f'basic-v58-{index+1:04d}',
        'category': category,
        'difficultyLevel': difficulty,
        'cognitiveType': cognitive,
        'learningObjective': objective,
        'estimatedSeconds': seconds,
        'similarityGroup': f'v58:{category}:{(index//4)+1:03d}',
        'optionFeedback': feedback(options, answer, '反応条件・酸化数・電子配置'),
        'referenceBasis': BASIC_REF,
    }
    if negative:
        item['negativeQuestion'] = True
        item['singleCorrectVerified'] = True
    return item


def hard_targets():
    # Existing hard answer counts are A71/B72/C69/D68. Add A14/B13/C16/D17 -> 85 each.
    remaining = [14, 13, 16, 17]
    result = []
    cursor = 0
    for _ in range(60):
        for step in range(4):
            p = (cursor + step) % 4
            if remaining[p] > 0:
                result.append(p)
                remaining[p] -= 1
                cursor = (p + 1) % 4
                break
    assert remaining == [0, 0, 0, 0]
    return result

